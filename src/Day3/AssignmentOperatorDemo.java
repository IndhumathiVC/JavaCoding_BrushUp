package Day3;

public class AssignmentOperatorDemo {
    public static void main(String [] args) {
        // 5) Assignment Operators are = += -= *=  /= %=

        //Ex:1 +=
        int a = 10;
        a+=5;     //a = a+5;
        System.out.println(a);

        //Ex:2 -=
        int b = 10;
        b-=5;     //b = b-5;
        System.out.println(b);

        //Ex:3 *=
        int c = 10;
        c*=2;    //c = c*2;
        System.out.println(c);

        //Ex:4 /=
        int d = 10;
        d/=2;   //d = d/2;
        System.out.println(d);

        //Ex:5 %=
        int e = 10;
        e%=2;     // e = e%2;
        System.out.println(e);
    }
}
