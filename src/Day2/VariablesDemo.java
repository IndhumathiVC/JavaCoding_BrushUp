package Day2;

public class VariablesDemo {
    public static void main(String[] args){
        int i;   // declaration of variable
        i = 16;  // assignment of value to a variable
        System.out.println(i);

        // different approaches of declaration of a variables
        int a = 100;             // first declared as 100
        System.out.println(a);   // will print a value as 100
        a = 200;                 // then declaring a value as 200
        System.out.println(a);   // printing a value as 200

        //different datatypes declaration -- Variables
        int age = 25;                // int
        System.out.println(age);

        String name = "Indhu";        // String
        System.out.println(name);

        float bookPrice = 50.10F;      // float
        System.out.println(bookPrice);

        char grade = 'A';              // char
        System.out.println(grade);

        boolean myBool = true;        // boolean
        System.out.println(myBool);

    }
}
